function mean(lst) {
    const result = lst.reduce(
        (a, b) => {
            if (isNaN(b)) {
                a[1] += b;
            } else {
                a[0] += +b;
            }

            return a;
        },
        [0, '']
    );

    return [result[0] / 10, result[1]];
}

